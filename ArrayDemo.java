public class ArrayDemo {
    public static void main(String args[])
    {
        int num[]={10,20,30,40};
        int i;
        System.out.println(num[0]);
        System.out.println(num[2]);
        for (i=0; i<=3; i++)
        {
            System.out.println(num[i]);
        }
    }
}
